with open("input.txt", "r") as f:
    rows = f.read().splitlines()

grid = []
i = 0
start_pos = (0, 0)
while rows[i] != "":
    grid.append(list(rows[i]))
    for j in range(len(grid[-1])):
        if grid[-1][j] == '@':
            start_pos = (i, j)
    i += 1

i += 1
moves = rows[i]

directions = {
    '^': (-1, 0),
    '>': (0, 1),
    'v': (1, 0),
    '<': (0, -1),
}

def can_push(grid, pos, dr):
    """Check if we can push a line of boxes starting at pos in direction dr.
    Returns (can_push, num_boxes) where num_boxes is how many boxes we'd push."""
    curr_pos = pos
    num_boxes = 0
    
    # Keep going until we hit something that's not a box
    while grid[curr_pos[0]][curr_pos[1]] == 'O':
        num_boxes += 1
        next_pos = (curr_pos[0] + dr[0], curr_pos[1] + dr[1])
        curr_pos = next_pos
    
    # If we hit an empty space, we can push all the boxes
    return grid[curr_pos[0]][curr_pos[1]] == '.', num_boxes

def try_move(pos, dr, grid):
    """Try to move the robot in direction dr. Returns new position."""
    next_pos = (pos[0] + dr[0], pos[1] + dr[1])
    next_cell = grid[next_pos[0]][next_pos[1]]
    
    if next_cell == '.':
        # Simple move to empty space
        grid[pos[0]][pos[1]] = '.'
        grid[next_pos[0]][next_pos[1]] = '@'
        return next_pos
    
    elif next_cell == 'O':
        # Try to push boxes
        pushable, num_boxes = can_push(grid, next_pos, dr)
        if pushable:
            # Clear all old box positions first
            curr = next_pos
            for _ in range(num_boxes):
                grid[curr[0]][curr[1]] = '.'
                curr = (curr[0] + dr[0], curr[1] + dr[1])
            
            # Now place boxes in new positions
            curr = (next_pos[0] + dr[0], next_pos[1] + dr[1])
            for _ in range(num_boxes):
                grid[curr[0]][curr[1]] = 'O'
                curr = (curr[0] + dr[0], curr[1] + dr[1])
            
            # Move robot
            grid[pos[0]][pos[1]] = '.'
            grid[next_pos[0]][next_pos[1]] = '@'
            return next_pos
    
    return pos

pos = start_pos
for move in moves:
    dr = directions[move]
    pos = try_move(pos, dr, grid)

total = 0
for j in range(len(grid)):
    for i in range(len(grid[0])):
        if grid[i][j] == 'O':
            total += 100 * j + i

print("\n".join(["".join(row) for row in grid]))
print(f"part1: {total}")